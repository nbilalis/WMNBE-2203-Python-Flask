# numeric operators

print(7 / 3)    # division
print(7 // 3)   # integer ivision
print(7 % 3)    # modulo (?)

print(2 ** 3)   # 2 in the power of 3
print(2 ^ 3)    # bitwise xor

x = 10
x += 1          # x = x + 1
x -= 2          # x = x - 2

# String operators

print('Python' + ' is ' + 'awesome' + '\n') # concatenation
print('-' * 50)
print('Python! ' * 3)
